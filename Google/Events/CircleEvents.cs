namespace Subgurim.Maps.Google.Events
{
    internal enum CircleEvents
    {
        /// <summary>
        /// This event is fired when the circle's center is changed.
        /// </summary>
        Center_Changed,

        /// <summary>
        /// This event is fired when the DOM click event is fired on the circle.
        /// </summary>
        Click,

        /// <summary>
        /// This event is fired when the DOM dblclick event is fired on the circle.
        /// </summary>
        DblClick,

        /// <summary>
        /// This event is fired when the DOM mousedown event is fired on the circle.
        /// </summary>
        MouseDown,

        /// <summary>
        /// This event is fired when the DOM mousemove event is fired on the circle.
        /// </summary>
        MouseMove,

        /// <summary>
        /// This event is fired on circle mouseout.
        /// </summary>
        MouseOut,

        /// <summary>
        /// This event is fired on circle mouseover.
        /// </summary>
        MouseOver,

        /// <summary>
        /// This event is fired when the DOM mouseup event is fired on the circle.
        /// </summary>
        MouseUp,

        /// <summary>
        /// This event is fired when the circle's radius is changed.
        /// </summary>
        Radius_Changed,

        /// <summary>
        /// This event is fired when the circle is right-clicked on.
        /// </summary>
        RightClick
    }
}
